    private void bActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bActualizarActionPerformed
        // TODO add your handling code here:

        int rows = tStock.getRowCount();
        double[] values = new double[rows];
        int[] prods = new int[rows];
        ArrayList<String> consultas = new ArrayList();
        ArrayList<String> consultasHistorico = new ArrayList();

        for (int i = 0; i < rows; i++) {
            String t = tStock.getValueAt(i, 4) + "";

            if (t.equals("")) {
                values[i] = 0;
            } else {
                values[i] = Double.parseDouble(t);
            }
            prods[i] = Integer.parseInt(tStock.getValueAt(i, 0) + "");

            if (values[i] != 0) {
                String consulta = "update stock set stock_actual = stock_actual + "
                        + values[i] + " where id_producto = " + prods[i] + "; ";

                String consultaHistorico = "insert into stock_mov values(0,"
                        + prods[i] + ", " + values[i] + ", '" + new Fecha().getFechaMySQL() + "', '"
                        + Hora.ahora().toString() + "');";

                consultas.add(consulta);
                consultasHistorico.add(consultaHistorico);
            }

        }
        for (int j = 0; j < consultas.size(); j++) {

            AdaptadorJDBC datos = new AdaptadorJDBC("root", "admin");

            try {
                datos.actualizar(consultas.get(j));
            } catch (Exception e) {
            }

            try {
                datos.actualizar(consultasHistorico.get(j));
            } catch (Exception e) {
            }

            try {
                datos.cerrar();
            } catch (Exception e) {
            }

        }
        actualizarTabla();
    }//GEN-LAST:event_bActualizarActionPerformed

