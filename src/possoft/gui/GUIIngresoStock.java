    public void actualizarTabla() {
        try {
            AdaptadorJDBC datos = new AdaptadorJDBC("root", "admin");

            String consulta = "select p.id_producto ,codigo, nombre, s.stock_actual \n"
                    + "from producto p \n"
                    + "join stock s on s.id_producto = p.id_producto \n"
                    + "where activo = 1\n"
                    + "order by codigo";

            datos.consultar(consulta);

            int cols = datos.resultados.getMetaData().getColumnCount();

            String[] titulos = new String[cols];

            /* for (int i = 0; i < cols; i++) {
                titulos[i] = datos.resultados.getMetaData().getColumnName(i + 1);
            }*/
            titulos = new String[]{"#", "Codigo", "Nombre", "Actual", "Cant."};

            ArrayList<String[]> registros = new ArrayList<>();

            while (datos.resultados.next()) {
                String[] temp = new String[5];

                temp[0] = datos.resultados.getString(1);
                temp[1] = datos.resultados.getString(2);
                temp[2] = datos.resultados.getString(3);
                temp[3] = datos.resultados.getString(4);
                temp[4] = "";

                registros.add(temp);
            }

            Object[][] data = new Object[registros.size()][];

            for (int i = 0; i < data.length; i++) {
                data[i] = registros.get(i);
            }

            tStock.setModel(new javax.swing.table.DefaultTableModel(
                    data,
                    titulos
            ));

            tStock.getColumnModel().getColumn(0).setPreferredWidth(10);
            tStock.getColumnModel().getColumn(0).setPreferredWidth(10);
            tStock.getColumnModel().getColumn(1).setPreferredWidth(100);
            tStock.getColumnModel().getColumn(2).setPreferredWidth(180);
            tStock.getColumnModel().getColumn(3).setPreferredWidth(70);
            tStock.getColumnModel().getColumn(4).setPreferredWidth(70);

            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(jLabel1.CENTER);
            tStock.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
            tStock.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);

            DefaultTableCellRenderer color = new DefaultTableCellRenderer();
            color.setBackground(new Color(23, 180, 252));
            color.setHorizontalAlignment(jLabel1.CENTER);
            color.setForeground(Color.WHITE);
            tStock.getColumnModel().getColumn(4).setCellRenderer(color);

            jScrollPane1.setViewportView(tStock);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

